export async function updateEventItemById(input: {
  id: string
  userId: string
  title?: string
  description?: string | null
  date?: string
  status?: EventStatus
  allDay?: boolean
}) {
  const existing = await prisma.eventItem.findUnique({
    where: { id: input.id },
  })
  if (!existing) return null
  if (existing.userId !== input.userId) return 'forbidden' as const

  return prisma.eventItem.update({
    where: { id: input.id },
    data: {
      ...(input.title !== undefined ? { title: input.title } : {}),
      ...(input.description !== undefined ? { description: input.description } : {}),
      ...(input.date !== undefined ? { eventDate: dateStringToUtcDate(input.date) } : {}),
      ...(input.status !== undefined ? { status: input.status } : {}),
      ...(input.allDay !== undefined ? { allDay: input.allDay } : {}),
    },
  })
}

function buildScopeWhere(scope: EventAccessScope): Prisma.EventItemWhereInput {
  if (scope.mode === 'user') {
    return { userId: scope.userId }
  }

  return {
    status: 'completed',
    sourceType: {
      in: ['post', 'thought', 'project', 'photo'],
    },
  }
}

export async function getCalendarSummary(
  scope: EventAccessScope,
  params: {
    from?: string
    to?: string
  }
) {
  const where: Prisma.EventItemWhereInput = {
    ...buildScopeWhere(scope),
  }

  if (params.from || params.to) {
    where.eventDate = {}
    if (params.from) {
      where.eventDate.gte = dateStringToUtcDate(params.from)
    }
    if (params.to) {
      where.eventDate.lte = endOfUtcDay(params.to)
    }
  }

  const [events, annotations] = await Promise.all([
    prisma.eventItem.findMany({
      where,
      orderBy: [{ eventDate: 'asc' }, { createdAt: 'asc' }],
    }),
    scope.mode === 'user'
      ? prisma.calendarAnnotation.findMany({
          where: {
            userId: scope.userId,
            ...(params.from || params.to
              ? {
                  date: {
                    ...(params.from ? { gte: dateStringToUtcDate(params.from) } : {}),
                    ...(params.to ? { lte: dateStringToUtcDate(params.to) } : {}),
                  },
                }
              : {}),
          },
          orderBy: { date: 'asc' },
        })
      : Promise.resolve([]),
  ])

  const grouped = new Map<
    string,
    {
      totalCount: number
      plannedCount: number
      completedCount: number
      canceledCount: number
      sourceTypes: Set<EventSourceType>
      annotationLabel: string | null
    }
  >()

  for (const event of events) {
    const key = toDateString(event.eventDate)
    const current =
      grouped.get(key) ??
      {
        totalCount: 0,
        plannedCount: 0,
        completedCount: 0,
        canceledCount: 0,
        sourceTypes: new Set<EventSourceType>(),
        annotationLabel: null,
      }

    current.totalCount += 1
    current.sourceTypes.add(event.sourceType)
    if (event.status === 'planned') current.plannedCount += 1
    if (event.status === 'completed') current.completedCount += 1
    if (event.status === 'canceled') current.canceledCount += 1
    grouped.set(key, current)
  }

  for (const annotation of annotations) {
    const key = toDateString(annotation.date)
    const current =
      grouped.get(key) ??
      {
        totalCount: 0,
        plannedCount: 0,
        completedCount: 0,
        canceledCount: 0,
        sourceTypes: new Set<EventSourceType>(),
        annotationLabel: null,
      }
    current.annotationLabel = annotation.label
    grouped.set(key, current)
  }

  return [...grouped.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([date, summary]) => ({
      date,
      totalCount: summary.totalCount,
      plannedCount: summary.plannedCount,
      completedCount: summary.completedCount,
      canceledCount: summary.canceledCount,
      annotationLabel: summary.annotationLabel,
      sourceTypes: [...summary.sourceTypes],
    }))
}

export async function getCalendarEventList(
  scope: EventAccessScope,
  params: {
    from?: string
    to?: string
  }
) {
  const where: Prisma.EventItemWhereInput = {
    ...buildScopeWhere(scope),
  }

  if (params.from || params.to) {
    where.eventDate = {}
    if (params.from) {
      where.eventDate.gte = dateStringToUtcDate(params.from)
    }
    if (params.to) {
      where.eventDate.lte = endOfUtcDay(params.to)
    }
  }

  const events = await prisma.eventItem.findMany({
    where,
    orderBy: [{ eventDate: 'asc' }, { createdAt: 'asc' }],
  })

  return events.map(serializeEventItem)
}

export async function getCalendarDay(
  scope: EventAccessScope,
  date: string
) {
  const [events, annotation] = await Promise.all([
    prisma.eventItem.findMany({
      where: {
        ...buildScopeWhere(scope),
        eventDate: {
          gte: dateStringToUtcDate(date),
          lte: endOfUtcDay(date),
        },
      },
      orderBy: [{ eventDate: 'asc' }, { createdAt: 'asc' }],
    }),
    scope.mode === 'user'
      ? prisma.calendarAnnotation.findUnique({
          where: {
            userId_date: {
              userId: scope.userId,
              date: dateStringToUtcDate(date),
            },
          },
        })
      : Promise.resolve(null),
  ])

  const summary = {
    date,
    totalCount: events.length,
    plannedCount: events.filter((event) => event.status === 'planned').length,
    completedCount: events.filter((event) => event.status === 'completed').length,
    canceledCount: events.filter((event) => event.status === 'canceled').length,
    annotationLabel: annotation?.label ?? null,
    sourceTypes: [...new Set(events.map((event) => event.sourceType))],
  }

  return {
    date,
    summary,
    annotation: annotation
      ? {
          id: annotation.id,
          date: toDateString(annotation.date),
          label: annotation.label,
          createdAt: annotation.createdAt.toISOString(),
          updatedAt: annotation.updatedAt.toISOString(),
        }
      : null,
    events: events.map(serializeEventItem),
  }
}

export async function createQuickCalendarEntry(input: QuickCreateInput) {
  const now = new Date()
  const inferredSourceType = inferSourceType(input.rawText)
  const targetDate = parseExplicitDate(input.rawText, now) ?? input.defaultDate ?? toDateString(now)
  const inferredStatus = inferStatus(input.rawText, inferredSourceType, targetDate, now)
  const title = extractTitle(input.rawText, inferredSourceType)
  const normalizedDescription = input.rawText.trim()

  if (inferredSourceType === 'post') {
    let slugBase = generateSlug(title)
    if (!slugBase) {
      slugBase = `post-${Date.now()}`
    }

    const existing = await prisma.post.findUnique({
      where: { slug: slugBase },
      select: { id: true },
    })

    const slug = existing ? `${slugBase}-${Date.now()}` : slugBase
    const post = await prisma.post.create({
      data: {
        authorId: input.userId,
        slug,
        title,
        content: normalizedDescription,
        published: false,
      },
    })

    const event = await upsertLinkedEvent({
      userId: input.userId,
      title,
      description: normalizedDescription,
      sourceType: 'post',
      sourceId: post.id,
      status: 'planned',
      eventDate: dateStringToUtcDate(targetDate),
      allDay: true,
      entryMethod: input.sourceInputType === 'voice' ? 'ai_voice' : 'ai_text',
      autoGenerated: false,
      payload: buildPayloadWithJumpUrl(
        {
          slug: post.slug,
          published: false,
        },
        `/blog/editor/${post.id}`
      ),
    })

    return {
      event: serializeEventItem(event),
      inferredSourceType,
      inferredStatus: 'planned' as EventStatus,
      targetDate,
      linkedEntity: {
        type: 'post' as const,
        id: post.id,
        jumpUrl: `/blog/editor/${post.id}`,
      },
      note: '已创建博客草稿，并关联到对应日期。出于保守策略，不会直接自动发布。',
    }
  }

  if (inferredSourceType === 'thought') {
    const thought = await prisma.thought.create({
      data: {
        authorId: input.userId,
        content: normalizedDescription,
        images: [],
      },
    })

    const event = await upsertLinkedEvent({
      userId: input.userId,
      title: buildThoughtTitle(normalizedDescription),
      description: normalizedDescription,
      sourceType: 'thought',
      sourceId: thought.id,
      status: inferredStatus,
      eventDate: dateStringToUtcDate(targetDate),
      allDay: true,
      entryMethod: input.sourceInputType === 'voice' ? 'ai_voice' : 'ai_text',
      autoGenerated: false,
      payload: buildPayloadWithJumpUrl(null, `/thoughts/${thought.id}/edit`),
    })

    return {
      event: serializeEventItem(event),
      inferredSourceType,
      inferredStatus,
      targetDate,
      linkedEntity: {
        type: 'thought' as const,
        id: thought.id,
        jumpUrl: `/thoughts/${thought.id}/edit`,
      },
      note: inferredStatus === 'planned' ? '已记录想法，并作为计划事件挂到对应日期。' : '已记录想法并聚合到当天事件流。',
    }
  }

  if (inferredSourceType === 'project') {
    const project = await prisma.projectEntry.create({
      data: {
        userId: input.userId,
        title,
        description: normalizedDescription,
        status: inferredStatus === 'planned' ? 'planned' : 'active',
        startedAt: dateStringToUtcDate(targetDate),
      },
    })

    const event = await upsertLinkedEvent({
      userId: input.userId,
      title,
      description: normalizedDescription,
      sourceType: 'project',
      sourceId: project.id,
      status: inferredStatus,
      eventDate: project.startedAt ?? project.createdAt,
      allDay: true,
      entryMethod: input.sourceInputType === 'voice' ? 'ai_voice' : 'ai_text',
      autoGenerated: false,
      payload: buildPayloadWithJumpUrl(
        {
          status: project.status,
        },
        '/projects'
      ),
    })

    return {
      event: serializeEventItem(event),
      inferredSourceType,
      inferredStatus,
      targetDate,
      linkedEntity: {
        type: 'project' as const,
        id: project.id,
        jumpUrl: '/projects',
      },
      note: '已创建项目记录，并加入当日事件流。',
    }
  }

  if (inferredSourceType === 'photo') {
    const photo = await prisma.photoEntry.create({
      data: {
        userId: input.userId,
        title,
        description: normalizedDescription,
        takenAt: dateStringToUtcDate(targetDate),
      },
    })

    const event = await upsertLinkedEvent({
      userId: input.userId,
      title,
      description: normalizedDescription,
      sourceType: 'photo',
      sourceId: photo.id,
      status: inferredStatus,
      eventDate: photo.takenAt ?? photo.createdAt,
      allDay: true,
      entryMethod: input.sourceInputType === 'voice' ? 'ai_voice' : 'ai_text',
      autoGenerated: false,
      payload: buildPayloadWithJumpUrl(null, '/pictures'),
    })

    return {
      event: serializeEventItem(event),
      inferredSourceType,
      inferredStatus,
      targetDate,
      linkedEntity: {
        type: 'photo' as const,
        id: photo.id,
        jumpUrl: '/pictures',
      },
      note: '已先记录一条照片事项。若要真正展示图片，可稍后在今日事件页上传文件补全。',
    }
  }

  const event = await createManualEvent({
    userId: input.userId,
    title,
    description: normalizedDescription,
    date: targetDate,
    status: inferredStatus,
    allDay: true,
    sourceType: 'manual',
  })

  return {
    event: serializeEventItem(event),
    inferredSourceType,
    inferredStatus,
    targetDate,
    linkedEntity: null,
    note: '已按通用事件记录，后续可在当天页继续补充细节或改为具体业务项。',
  }
}
