async function findEventBySource(sourceType: EventSourceType, sourceId: string) {
  return prisma.eventItem.findFirst({
    where: {
      sourceType,
      sourceId,
    },
    orderBy: {
      createdAt: 'asc',
    },
  })
}

async function upsertLinkedEvent(input: {
  userId: string
  title: string
  description?: string | null
  sourceType: EventSourceType
  sourceId: string
  status: EventStatus
  eventDate: Date
  allDay?: boolean
  entryMethod?: string
  autoGenerated?: boolean
  payload?: EventPayloadInput | null
}) {
  const existing = await findEventBySource(input.sourceType, input.sourceId)
  const payloadJson = toJsonPayload(input.payload ?? null)
  const data: Prisma.EventItemUncheckedCreateInput = {
    userId: input.userId,
    title: input.title,
    description: input.description ?? null,
    sourceType: input.sourceType,
    sourceId: input.sourceId,
    status: input.status,
    eventDate: input.eventDate,
    allDay: input.allDay ?? true,
    entryMethod: input.entryMethod ?? 'manual',
    autoGenerated: input.autoGenerated ?? false,
  }
  if (payloadJson !== null) {
    data.payloadJson = payloadJson
  }

  if (existing) {
    return prisma.eventItem.update({
      where: { id: existing.id },
      data: {
        ...data,
        ...(payloadJson === null ? { payloadJson: Prisma.JsonNull } : {}),
      },
    })
  }

  return prisma.eventItem.create({
    data,
  })
}

export async function removeEventsForSource(sourceType: EventSourceType, sourceId: string) {
  await prisma.eventItem.deleteMany({
    where: {
      sourceType,
      sourceId,
    },
  })
}

export async function syncPostEvent(post: Post) {
  const jumpUrl = post.published ? `/posts/${post.slug || post.id}` : `/blog/editor/${post.id}`
  const payload = buildPayloadWithJumpUrl(
    {
      slug: post.slug || post.id,
      published: post.published,
    },
    jumpUrl
  )

  const existing = await findEventBySource('post', post.id)
  if (!post.published) {
    if (existing?.status === 'planned') {
      await prisma.eventItem.update({
        where: { id: existing.id },
        data: {
          title: post.title,
          description: post.excerpt ?? null,
          payloadJson: toJsonPayload(payload) ?? Prisma.JsonNull,
        },
      })
    } else if (existing?.autoGenerated) {
      await prisma.eventItem.delete({
        where: { id: existing.id },
      })
    }
    return
  }

  await upsertLinkedEvent({
    userId: post.authorId,
    title: post.title,
    description: post.excerpt ?? null,
    sourceType: 'post',
    sourceId: post.id,
    status: 'completed',
    eventDate: post.publishedAt ?? post.createdAt,
    allDay: true,
    entryMethod: 'system',
    autoGenerated: true,
    payload,
  })
}

function buildThoughtTitle(content: string): string {
  const condensed = content.replace(/\s+/g, ' ').trim()
  if (!condensed) return '记录想法'
  return (condensed.length > 48 ? `${condensed.slice(0, 48)}…` : condensed).slice(0, 120)
}

export async function syncThoughtEvent(thought: Thought) {
  if (!thought.authorId) return

  await upsertLinkedEvent({
    userId: thought.authorId,
    title: buildThoughtTitle(thought.content),
    description: thought.content.slice(0, 280) || null,
    sourceType: 'thought',
    sourceId: thought.id,
    status: 'completed',
    eventDate: thought.createdAt,
    allDay: true,
    entryMethod: 'system',
    autoGenerated: true,
    payload: buildPayloadWithJumpUrl(null, `/thoughts/${thought.id}/edit`),
  })
}

function mapProjectStatusToEventStatus(status: string): EventStatus {
  return status === 'planned' ? 'planned' : 'completed'
}

export async function syncProjectEvent(project: ProjectEntry) {
  if (!project.userId) return

  const jumpUrl = project.href?.trim() || '/projects'
  await upsertLinkedEvent({
    userId: project.userId,
    title: project.title,
    description: project.description ?? null,
    sourceType: 'project',
    sourceId: project.id,
    status: mapProjectStatusToEventStatus(project.status),
    eventDate: project.startedAt ?? project.createdAt,
    allDay: true,
    entryMethod: 'manual',
    autoGenerated: true,
    payload: buildPayloadWithJumpUrl(
      {
        href: project.href ?? null,
        status: project.status,
      },
      jumpUrl
    ),
  })
}

